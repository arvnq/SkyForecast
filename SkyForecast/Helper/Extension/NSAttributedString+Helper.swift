extension NSAttributedString {
    static func setupAttributedString(for field: String, using value: Strong) -> NSAttributedString {
        let regularAttribute: [NSAttributedString.key: Any] = [
            .font: UIFont.systemFont(ofSize: 14.0)
        ]
    }
}
